def running(windowSurface,ver,hor):
    observation_space=2
    action_space = 4
    dqn_solver = DQNSolver(observation_space, action_space)
    run = 0
    while True:
        run += 1
        xPos = 0
        yPos = 0
        state = [xPos, yPos]
        state = np.reshape(state,[1,observation_space])
        step = 0
        while True:
            step += 1
            screen.blit(windowSurface, (0,0))
            screen.blit(player.surf, (10+xPos*40, 10+yPos*40))
            pygame.display.flip()
            action = dqn_solver.act(state)
            xPos, yPos, reward, terminal = Player().move(action, xPos, yPos,ver,hor, step)
            reward = reward if not terminal else -reward
            state_next = [xPos,yPos]
            state_next = np.reshape(state_next,[1,observation_space])
            dqn_solver.remember(state,action,reward,state_next,terminal)
            state = state_next
            if terminal:
                print("nice it finished")
                break
            dqn_solver.experience_replay()

